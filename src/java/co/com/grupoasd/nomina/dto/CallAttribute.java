package co.com.grupoasd.nomina.dto;

/**
 *
 * @author ASD
 */
public class CallAttribute {

    private String name;
    private Object value;

    /**
     * Default constructor
     */
    public CallAttribute() {
    }

    /**
     * Constructor with values
     *
     * @param name
     * @param value
     */
    public CallAttribute(String name, Object value) {
        this.name = name;
        this.value = value;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the value
     */
    public Object getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(Object value) {
        this.value = value;
    }

}
