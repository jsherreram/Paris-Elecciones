package co.com.grupoasd.nomina.modelo.wrapper;

import co.com.grupoasd.nomina.modelo.Empleado;
import co.com.grupoasd.nomina.modelo.EmpleadoPruebaEstado;

/**
 *
 * @author ASD
 */
public class EmpleadoPruebaEstadoCargo extends EmpleadoPruebaEstado {

    /**
     * 
     */
    private Empleado empleado;
    private int salones;
    private double distancia;
    private int viaja;

    /**
     * @return the empleado
     */
    public Empleado getEmpleado() {
        return empleado;
    }

    /**
     * @param empleado the empleado to set
     */
    public void setEmpleado(Empleado empleado) {
        this.empleado = empleado;
    }

    /**
     * @return the salones
     */
    public int getSalones() {
        return salones;
    }

    /**
     * @param salones the salones to set
     */
    public void setSalones(int salones) {
        this.salones = salones;
    }

    /**
     * @return the viaja
     */
    public int getViaja() {
        return viaja;
    }

    /**
     * @param viaja the viaja to set
     */
    public void setViaja(int viaja) {
        this.viaja = viaja;
    }

    /**
     * @return the distancia
     */
    public double getDistancia() {
        return distancia;
    }

    /**
     * @param distancia the distancia to set
     */
    public void setDistancia(double distancia) {
        this.distancia = distancia;
    }
    
}
