package co.com.grupoasd.nomina.modelo.wrapper;

import co.com.grupoasd.nomina.modelo.Nombramiento;
import co.com.grupoasd.nomina.modelo.Sitio;
import co.com.grupoasd.nomina.modelo.ZonaIcfes;
import java.io.Serializable;

/**
 *
 * @author ASD
 */
public class NombramientoCargo extends Nombramiento implements Serializable {
    
    /**
     * Parametros completos asociados al nombramiento
     */
    private ZonaIcfes zonaIcfes;
    private Sitio divipol;
    private String nrodocEmpleado;
    private String codigoDepartamento;

    /**
     * @return the zona
     */
    public ZonaIcfes getZonaIcfes() {
        return zonaIcfes;
    }

    /**
     * @param zonaIcfes the zona to set
     */
    public void setZonaIcfes(ZonaIcfes zonaIcfes) {
        this.zonaIcfes = zonaIcfes;
    }

    /**
     * @return the divipol
     */
    public Sitio getDivipol() {
        return divipol;
    }

    /**
     * @param divipol the divipol to set
     */
    public void setDivipol(Sitio divipol) {
        this.divipol = divipol;
    }

    /**
     * @return the nrodocEmpleado
     */
    public String getNrodocEmpleado() {
        return nrodocEmpleado;
    }

    /**
     * @param nrodocEmpleado the nrodocEmpleado to set
     */
    public void setNrodocEmpleado(String nrodocEmpleado) {
        this.nrodocEmpleado = nrodocEmpleado;
    }

    /**
     * @return the codigoDepartamento
     */
    public String getCodigoDepartamento() {
        return codigoDepartamento;
    }

    /**
     * @param codigoDepartamento the codigoDepartamento to set
     */
    public void setCodigoDepartamento(String codigoDepartamento) {
        this.codigoDepartamento = codigoDepartamento;
    }

}
