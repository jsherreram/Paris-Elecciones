package co.com.grupoasd.nomina.modelo;

/**
 *
 * @author Grupo ASD
 */
public class VacantesSitioCargo {
    
    private String codigoPuesto;
    private String nombrePuesto;
    private String direccionPuesto;
    private Integer vacantes;
    private String codigoZona;
    //propiedades de busqueda
    private String codigoCargo;
    private Integer codigoEvento;
    private String codigoMunicipio;

    /**
     * @return the codigoPuesto
     */
    public String getCodigoPuesto() {
        return codigoPuesto;
    }

    /**
     * @param codigoPuesto the codigoPuesto to set
     */
    public void setCodigoPuesto(String codigoPuesto) {
        this.codigoPuesto = codigoPuesto;
    }

    /**
     * @return the nombrePuesto
     */
    public String getNombrePuesto() {
        return nombrePuesto;
    }

    /**
     * @param nombrePuesto the nombrePuesto to set
     */
    public void setNombrePuesto(String nombrePuesto) {
        this.nombrePuesto = nombrePuesto;
    }

    /**
     * @return the direccionPuesto
     */
    public String getDireccionPuesto() {
        return direccionPuesto;
    }

    /**
     * @param direccionPuesto the direccionPuesto to set
     */
    public void setDireccionPuesto(String direccionPuesto) {
        this.direccionPuesto = direccionPuesto;
    }

    /**
     * @return the vacantes
     */
    public Integer getVacantes() {
        return vacantes;
    }

    /**
     * @param vacantes the vacantes to set
     */
    public void setVacantes(Integer vacantes) {
        this.vacantes = vacantes;
    }

    /**
     * @return the codigoZona
     */
    public String getCodigoZona() {
        return codigoZona;
    }

    /**
     * @param codigoZona the codigoZona to set
     */
    public void setCodigoZona(String codigoZona) {
        this.codigoZona = codigoZona;
    }

    /**
     * @return the codigoCargo
     */
    public String getCodigoCargo() {
        return codigoCargo;
    }

    /**
     * @param codigoCargo the codigoCargo to set
     */
    public void setCodigoCargo(String codigoCargo) {
        this.codigoCargo = codigoCargo;
    }

    /**
     * @return the codigoEvento
     */
    public Integer getCodigoEvento() {
        return codigoEvento;
    }

    /**
     * @param codigoEvento the codigoEvento to set
     */
    public void setCodigoEvento(Integer codigoEvento) {
        this.codigoEvento = codigoEvento;
    }

    /**
     * @return the codigoMunicipio
     */
    public String getCodigoMunicipio() {
        return codigoMunicipio;
    }

    /**
     * @param codigoMunicipio the codigoMunicipio to set
     */
    public void setCodigoMunicipio(String codigoMunicipio) {
        this.codigoMunicipio = codigoMunicipio;
    }
          

}
