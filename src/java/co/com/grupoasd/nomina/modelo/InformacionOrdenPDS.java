package co.com.grupoasd.nomina.modelo;

import java.util.List;

/**
 *
 * @author Grupo ASD
 */
public class InformacionOrdenPDS {
    private Long idOrden;
    private String estado;
    private Long codigoSitio;
    private String nombreSitio;
    private List<DetalleOrden> detallesPersonal;
    

    /**
     * @return the idOrden
     */
    public Long getIdOrden() {
        return idOrden;
    }

    /**
     * @param idOrden the idOrden to set
     */
    public void setIdOrden(Long idOrden) {
        this.idOrden = idOrden;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

    /**
     * @return the codigoSitio
     */
    public Long getCodigoSitio() {
        return codigoSitio;
    }

    /**
     * @param codigoSitio the codigoSitio to set
     */
    public void setCodigoSitio(Long codigoSitio) {
        this.codigoSitio = codigoSitio;
    }

    /**
     * @return the nombreSitio
     */
    public String getNombreSitio() {
        return nombreSitio;
    }

    /**
     * @param nombreSitio the nombreSitio to set
     */
    public void setNombreSitio(String nombreSitio) {
        this.nombreSitio = nombreSitio;
    }

    /**
     * @return the detallesPersonal
     */
    public List<DetalleOrden> getDetallesPersonal() {
        return detallesPersonal;
    }

    /**
     * @param detallesPersonal the detallesPersonal to set
     */
    public void setDetallesPersonal(List<DetalleOrden> detallesPersonal) {
        this.detallesPersonal = detallesPersonal;
    }
}
