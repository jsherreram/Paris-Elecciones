package co.com.grupoasd.nomina.modelo;

/**
 *
 * @author CristianAlexander
 */
public class EstadosOrden {
    private Long idEstadoOrden;
    private String codigoEstado;
    private String descripcion;
    private Long activo;

    /**
     * @return the idEstadoOrden
     */
    public Long getIdEstadoOrden() {
        return idEstadoOrden;
    }

    /**
     * @param idEstadoOrden the idEstadoOrden to set
     */
    public void setIdEstadoOrden(Long idEstadoOrden) {
        this.idEstadoOrden = idEstadoOrden;
    }

    /**
     * @return the codigoEstado
     */
    public String getCodigoEstado() {
        return codigoEstado;
    }

    /**
     * @param codigoEstado the codigoEstado to set
     */
    public void setCodigoEstado(String codigoEstado) {
        this.codigoEstado = codigoEstado;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the activo
     */
    public Long getActivo() {
        return activo;
    }

    /**
     * @param activo the activo to set
     */
    public void setActivo(Long activo) {
        this.activo = activo;
    }
}
