package co.com.grupoasd.nomina.modelo;

import java.util.List;

/**
 *
 * @author Grupo ASD
 */
public class OrdenCompleta {
    private Orden orden;
    private List<DetalleOrden> detalles;
    private List<InformacionDespachosOrden> despachos;

    /**
     * @return the orden
     */
    public Orden getOrden() {
        return orden;
    }

    /**
     * @param orden the orden to set
     */
    public void setOrden(Orden orden) {
        this.orden = orden;
    }

    /**
     * @return the detalles
     */
    public List<DetalleOrden> getDetalles() {
        return detalles;
    }

    /**
     * @param detalles the detalles to set
     */
    public void setDetalles(List<DetalleOrden> detalles) {
        this.detalles = detalles;
    }

    /**
     * @return the despachos
     */
    public List<InformacionDespachosOrden> getDespachos() {
        return despachos;
    }

    /**
     * @param despachos the despachos to set
     */
    public void setDespachos(List<InformacionDespachosOrden> despachos) {
        this.despachos = despachos;
    }
}
